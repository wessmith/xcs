#import "SomeObject.h"

@implementation SomeObject

+ (NSInteger)sumOfNumber:(NSInteger)number adding:(NSInteger)add {
    return number + add;
}

+ (NSInteger)resultOfNumber:(NSInteger)number subtracting:(NSInteger)subtract {
    return number - subtract;
}

+ (NSInteger)resultOfNumber:(NSInteger)number multipliedBy:(NSInteger)multiplier {
    return number * multiplier;
}

@end
